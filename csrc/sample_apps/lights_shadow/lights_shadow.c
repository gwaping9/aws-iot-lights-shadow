void u8g_prepare(void) {
  u8g_SetFont(&u8g, u8g_font_5x8);
  u8g_SetFontRefHeightExtendedText(&u8g);
  u8g_SetDefaultForegroundColor(&u8g);
  u8g_SetFontPosTop(&u8g);
}

// screensaver global variables
u8g_uint_t ballXPosition = 0;
u8g_uint_t ballYPosition = 0;
uint8_t rad = 3;
uint8_t padding = 4;
bool movingRight = true;
bool movingDown = true;

void displayScreenSaver(void){
	u8g_prepare();
	u8g_FirstPage(&u8g);
	do
	{
		if(movingRight){
			ballXPosition++;
			if( ballXPosition > OLED_WIDTH - padding){
				movingRight = false;
			}	
		}else{
			ballXPosition--;
			if( ballXPosition < 1){
				movingRight = true;
			}
		}

		if(movingDown){
			ballYPosition++;
			if( ballYPosition > OLED_HEIGHT - padding){
				movingDown = false;
			}	
		}else{
			ballYPosition--;
			if( ballYPosition < 1){
				movingDown = true;
			}
		}
		u8g_DrawDisc(&u8g, ballXPosition, ballYPosition, rad, U8G_DRAW_ALL);
	} while ( u8g_NextPage(&u8g));
	// refresh screen after some delay
	u8g_Delay(100);
}

void displayText(struct OutputTxt textArray[], int sleepTime){
	// setup the dislay
	u8g_prepare();
	int i = 0;

	// display the text
	u8g_FirstPage(&u8g);
    do{
    	// loop through struct array and display text to screen
    	for (i = 0; i < (int)sizeof(textArray); i++){
    		u8g_DrawStr(&u8g, textArray[i].xPosition, textArray[i].yPosition, textArray[i].text);
    	}
	}while ( u8g_NextPage(&u8g) );

    // sleep based on variable passed
    sleep(sleepTime);
}

void displayTime(void){
	// get the time
	struct timeval tv;
 	struct tm* ptm;
 	char date_string[40];
 	char time_string[40];

 	// obtain the time of day, and convert it to a tm struct
 	gettimeofday (&tv, NULL);
 	ptm = localtime (&tv.tv_sec);

 	// format the date and time, down to a single second
 	strftime (date_string, sizeof (date_string), "%m/%d/%Y", ptm);
 	strftime (time_string, sizeof (date_string), "%H:%M:%S", ptm);

	u8g_prepare();
	u8g_FirstPage(&u8g);
	do
	{
		u8g_DrawStr(&u8g, 1, 10, date_string);
		u8g_DrawStr(&u8g, 10, 23, time_string);

	} while ( u8g_NextPage(&u8g));
	// refresh screen after some delay
	u8g_Delay(100);
}

void lightsController_Callback(const char *pJsonString, uint32_t JsonStringDataLen, jsonStruct_t *pContext) {
	if (pContext != NULL) {
		INFO("Delta - Lights state changed to %d", *(bool *)(pContext->pData));
	}

	// display new message
	struct OutputTxt textArray [] = {
		{1, 10, "NEW AWS"},
		{1, 20, "MESSAGE!"}
	};
	displayText(textArray, 3);

	// get new state from pContact->pData
	bool state = *(bool *)(pContext->pData);
	u8g_prepare();
	u8g_FirstPage(&u8g);
	do {
		if(state){
			digitalWrite(lightsPin, HIGH);
			u8g_DrawXBM(&u8g, 0, 0, aws_on_width, aws_on_height, aws_on_bits);
		}else{
			digitalWrite(lightsPin, LOW);
			u8g_DrawXBM(&u8g, 0, 0, aws_off_width, aws_off_height, aws_off_bits);
		}
	} while ( u8g_NextPage(&u8g));
	// refresh screen after some delay
	u8g_Delay(100);

	// show logo for 5 seconds before going back to screensaver
    sleep(5);
}

char certDirectory[PATH_MAX + 1] = "../../certs";
char HostAddress[255] = AWS_IOT_MQTT_HOST;
uint32_t port = AWS_IOT_MQTT_PORT;
uint8_t numPubs = 5;

#define MAX_LENGTH_OF_UPDATE_JSON_BUFFER 200

int main(int argc, char** argv) {

	// setup u8glib spi and wiringPi - CS on pin 10, A0 on 16 and Reset on 15
	u8g_InitHWSPI(&u8g, &u8g_dev_ssd1306_64x48_2x_hw_spi, 10, 16, 15);
    wiringPiSetup();

    // setup pin 25 for output (turns on/off relay)
    pinMode(lightsPin, OUTPUT);
    
    // time variables
    time_t now_t, previous_t;
    time(&now_t);
    time(&previous_t);
    double diff_t;

    IoT_Error_t rc = NONE_ERROR;
	int32_t i = 0;

	MQTTClient_t mqttClient;
	aws_iot_mqtt_init(&mqttClient);

	char JsonDocumentBuffer[MAX_LENGTH_OF_UPDATE_JSON_BUFFER];
	size_t sizeOfJsonDocumentBuffer = sizeof(JsonDocumentBuffer) / sizeof(JsonDocumentBuffer[0]);
	char *pJsonStringToUpdate;

	bool lightsOn = digitalRead(lightsPin);
	jsonStruct_t lightsController;
	lightsController.cb = lightsController_Callback;
	lightsController.pData = &lightsOn;
	lightsController.pKey = "lightsOn";
	lightsController.type = SHADOW_JSON_BOOL;

	char rootCA[PATH_MAX + 1];
	char clientCRT[PATH_MAX + 1];
	char clientKey[PATH_MAX + 1];
	char CurrentWD[PATH_MAX + 1];
	char cafileName[] = AWS_IOT_ROOT_CA_FILENAME;
	char clientCRTName[] = AWS_IOT_CERTIFICATE_FILENAME;
	char clientKeyName[] = AWS_IOT_PRIVATE_KEY_FILENAME;

	INFO("\nAWS IoT SDK Version(dev) %d.%d.%d-%s\n", VERSION_MAJOR, VERSION_MINOR, VERSION_PATCH, VERSION_TAG);

	getcwd(CurrentWD, sizeof(CurrentWD));
	sprintf(rootCA, "%s/%s/%s", CurrentWD, certDirectory, cafileName);
	sprintf(clientCRT, "%s/%s/%s", CurrentWD, certDirectory, clientCRTName);
	sprintf(clientKey, "%s/%s/%s", CurrentWD, certDirectory, clientKeyName);

	DEBUG("Using rootCA %s", rootCA);
	DEBUG("Using clientCRT %s", clientCRT);
	DEBUG("Using clientKey %s", clientKey);

	ShadowParameters_t sp = ShadowParametersDefault;
	sp.pMyThingName = AWS_IOT_MY_THING_NAME;
	sp.pMqttClientId = AWS_IOT_MQTT_CLIENT_ID;
	sp.pHost = HostAddress;
	sp.port = port;
	sp.pClientCRT = clientCRT;
	sp.pClientKey = clientKey;
	sp.pRootCA = rootCA;

	INFO("Shadow Init");
	rc = aws_iot_shadow_init(&mqttClient);

	INFO("Shadow Connect");
	rc = aws_iot_shadow_connect(&mqttClient, &sp);

	if (NONE_ERROR != rc) {
		ERROR("Shadow Connection Error %d", rc);
	}

	rc = aws_iot_shadow_register_delta(&mqttClient, &lightsController);

	if (NONE_ERROR != rc) {
		ERROR("Shadow Register Delta Error");
	}

	// loop and publish a change in light status
	while(NONE_ERROR == rc) {
		displayScreenSaver();
		time(&now_t);
		diff_t = difftime(now_t, previous_t);
		if(diff_t > UPDATE_FREQUENCY){
			rc = aws_iot_shadow_yield(&mqttClient, 200);
			INFO("\n=======================================================================================\n");
			INFO("On Device: Lights state %s", lightsOn?"true":"false");

			rc = aws_iot_shadow_init_json_document(JsonDocumentBuffer, sizeOfJsonDocumentBuffer);
			if (rc == NONE_ERROR) {
				rc = aws_iot_shadow_add_reported(JsonDocumentBuffer, sizeOfJsonDocumentBuffer, 1, &lightsController);
				if (rc == NONE_ERROR) {
					rc = aws_iot_finalize_json_document(JsonDocumentBuffer, sizeOfJsonDocumentBuffer);
					if (rc == NONE_ERROR) {
						INFO("Update Shadow: %s", JsonDocumentBuffer);
						rc = aws_iot_shadow_update(&mqttClient, AWS_IOT_MY_THING_NAME, JsonDocumentBuffer, NULL,
						NULL, 4, true);
					}
				}
			}
			previous_t = now_t;
			INFO("*****************************************************************************************\n");	
		}
	}
	
	if (NONE_ERROR != rc) {
		ERROR("An error occurred in the loop %d", rc);
	}

	INFO("Disconnecting");
	rc = aws_iot_shadow_disconnect(&mqttClient);

	if (NONE_ERROR != rc) {
		ERROR("Disconnect error %d", rc);
	}

	return rc;
}
